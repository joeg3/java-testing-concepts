package org.example;

public class Library {

  public boolean libraryMethodReturnsTrue() {
    return true;
  }

  public int addNumbers(int x, int y) { return x + y;}

  public int multiplyNumbers(int x, int y) { return x * y;}

  public int divideNumbers(int x, int y) { return x / y;}

  public double computeCircleArea(double radius) {
    return Math.PI * radius * radius;
  }
}